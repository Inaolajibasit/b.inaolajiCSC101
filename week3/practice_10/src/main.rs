fn main() {
    // addition 
    let sum = 5550 + 7310;
    println!("the sum of 5550 and 7310 = {}",sum );

    // subtraction 
    let sub:f64 = 95.5 + 4.3;
    println!("the difference of 95.5 and 4.3 = {}",sub );

    // multipication 
    let product:f64 = 4.0 * 30.0;
    println!("the product of 4.0 and 30.0 = {}",product );

    // division 
    let quo:f64 = 56.7 / 32.2;
    println!("the sum of 56.7 and 32.2 = {}",quo );

    // remainder 
    let remainder:f64 = 43.0 % 5.0;
    println!("the remainder of 43 and 5 = {}",remainder );
}